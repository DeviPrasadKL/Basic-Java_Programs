class Cn {
	public static void main(String[] args) {
		// Declaration
		String name;
		int age;
		double height;
		char gender;

		// Initialization for Tom
		name = "Tom";
		age = 20;
		height = 5.0;
		gender = 'M';
		System.out.println("Name is " + name);
		System.out.println("The age is " + age + " years");
		System.out.println("The height is " + height + " feet");
		System.out.println("Gender is " + gender);

		// Innitialzation for Jerry
		name = "Jerry";
		age = 22;
		height = 4.0;
		gender = 'M';
		System.out.println("Name is " + name);
		System.out.println("The age is " + age + " years");
		System.out.println("The height is " + height + " feet");
		System.out.println("Gender is " + gender);

	}
}