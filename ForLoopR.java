//W a p to print number 1 to 5 in reverse order

class ForLoopR {
	public static void main(String[] args) {
		System.out.println("Program Started");
		for (int i = 5; i >= 1; i--) {
			System.out.println(i);
		}
		System.out.println("Program Ended");
	}
}

