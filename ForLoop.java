//W a p to print number 1 to 5

class ForLoop {
	public static void main(String[] args) {
		System.out.println("Program Started");
		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
		System.out.println("Program Ended");
	}
}

