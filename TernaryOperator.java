class TernaryOperator {
	public static void main(String[] args) {
		System.out.println("Program Started");
		int a = 100;
		int b = 20;
		int max = a > b ? a : b;
		System.out.println("Maximum of " + a + " and " + b + " is " + max);
		System.out.println("Program ended");
	}
}
/*
 * Program Started
 * Maximum of 100 and 20 is 100
 * Program ended
 */